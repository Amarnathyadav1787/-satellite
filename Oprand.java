class Oprand{

public static void main(String args[]){


    int a = 10;
    int b =++a;  //pre increment
    System.out.println("Pre increment valu");
    System.out.println(a);
    System.out.println(b);
    int c=a++;
    System.out.println("post increment valu");
    System.out.println(a);
    System.out.println(c);

    int d=--a;
    System.out.println("Pre decrement valu");
    System.out.println(a);
    System.out.println(d);

    int e= a--;
    System.out.println("Post decrement valu");
    System.out.println(a);
    System.out.println(e);


   



}

}